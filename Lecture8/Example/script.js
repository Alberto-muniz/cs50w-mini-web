// Declaramos una variable llamada counter
// Va a guardar el número de clics
// Empieza en 0
let counter = 0;


// Buscamos el botón con id="btn" en el HTML
document.querySelector("#btn").onclick = function () {

    // Cada vez que se hace clic:
    // aumentamos el contador en 1
    counter++;

    // Buscamos el elemento <h1> con id="message"
    // y cambiamos su contenido por el valor de counter
    document.querySelector("#message").innerHTML = counter;
};












































































































































